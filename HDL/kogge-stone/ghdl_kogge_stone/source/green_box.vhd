
library IEEE;
use IEEE.STD_LOGIC_1164.ALL;
use IEEE.STD_LOGIC_ARITH.ALL;
use IEEE.STD_LOGIC_UNSIGNED.ALL;

entity green_box is
    Port ( pi : in  STD_LOGIC;
           gi : in  STD_LOGIC;
           p  : out  STD_LOGIC;
	   g : out STD_LOGIC
	);
end green_box;

architecture behavioral of green_box is
begin

	p <= pi;
	g <= gi;	

end Behavioral;

